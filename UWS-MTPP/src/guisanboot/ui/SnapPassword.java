    private void myInit(SanBootView view,String ip,int port,String user,String passwd){
        syncView = view;
        
        this.jTextField3.setText(user);
        this.jPasswordField1.setText(passwd);
        
        regKeyboardAction();
        jPasswordField1.requestFocus();
        setupLanguage();
        
        jPasswordField1.addKeyListener(new java.awt.event.KeyAdapter(){
            public void keyPressed( KeyEvent e ){
                if( e.getKeyCode() == KeyEvent.VK_ENTER ){
                    jButton1ActionPerformed(null);
                }
            }
        });         
    }
    
    private void regKeyboardAction(){            
 
        jTextField3.registerKeyboardAction(
            new ActionListener(){
                public void actionPerformed(ActionEvent e){
                    jPasswordField1.requestFocus();
                }
            },
            KeyStroke.getKeyStroke(KeyEvent.VK_ENTER,0,true),
            //JComponent.WHEN_IN_FOCUSED_WINDOW
            JComponent.WHEN_ANCESTOR_OF_FOCUSED_COMPONENT
        );
    }
     
    private void setupLanguage(){
        setTitle(SanBootView.res.getString("ConnectDialog.dialog_snap.title"));
        
        jLabel3.setText(SanBootView.res.getString("ConnectDialog.label.text2")); //"User");
        jLabel4.setText(SanBootView.res.getString("ConnectDialog.label.text1")); //"Password"
        
        jButton1.setText(SanBootView.res.getString("common.button.ok"));
        jButton2.setText(SanBootView.res.getString("common.button.cancel"));
    }
    
