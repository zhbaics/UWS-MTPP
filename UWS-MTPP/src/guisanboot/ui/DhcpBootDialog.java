    private void setupHost(){
        //jComboBox1.addItem( "emboot" );
        //jComboBox1.addItem( "iboot" );
        //jComboBox1.addItem( "peboot" );
        bootMap.put("推荐模式", "emboot");
        bootMap.put("替代模式", "iboot");
        bootMap.put("PE启动", "peboot");
        Iterator itboot = bootMap.entrySet().iterator();
        while(itboot.hasNext()){
            Map.Entry entry = (Entry) itboot.next();
            jComboBox1.addItem(entry.getKey());
        }
        
        Vector list = view.initor.dhcpdb.getAllClient();
        int num = list.size();
        for(int i=0; i<num; i++ ){
            DhcpClientInfo clnt = (DhcpClientInfo)list.elementAt(i);
            if(host.getIP().equals(clnt.ip))
                jComboBox1.setSelectedItem( clnt.boottype );
        }
    }

