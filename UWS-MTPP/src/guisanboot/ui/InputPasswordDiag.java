    // ok button process
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String aPasswd = new String( jPasswordField3.getPassword() );
        if( aPasswd.equals("") ){
            JOptionPane.showMessageDialog(this,
                SanBootView.res.getString("InputPasswordDialog.errMsg.nonNewPasswd")
            );
            return;
        }
        
        if( Check.checkInput( aPasswd ) ){
            JOptionPane.showMessageDialog(this,
                SanBootView.res.getString("InputPasswordDialog.errMsg.badPasswd")
            );
            return;
        }
        
        if( aPasswd.getBytes().length >=32 ){
            JOptionPane.showMessageDialog(this,
                SanBootView.res.getString("InputPasswordDialog.errMsg.tooBigPasswd")
            );
            return;
        }
        
        if( !aPasswd.equals( right_passwd ) ){
            JOptionPane.showMessageDialog(this,
                SanBootView.res.getString("EditMirrorJobDialog.error.badPoolPasswd")
            );
            return;
        }
        
        this.passwd = aPasswd;
        
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed
    
