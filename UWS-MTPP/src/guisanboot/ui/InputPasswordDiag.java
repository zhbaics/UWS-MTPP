    private void myInit( SanBootView view,UWSrvNode uws,PoolWrapper pool ){
        this.view = view;
        this.right_passwd = pool.pool.getPool_passwd();

        this.jLabel2.setText( uws.getUws_ip()+" / "+pool.toString() );
        
        regKeyboardAction();
        setupLanguage();
    }
    
    private void regKeyboardAction(){    
        jPasswordField3.registerKeyboardAction(
            new ActionListener(){
                public void actionPerformed(ActionEvent e){
                    jButton1.requestFocus();
                }
            },
            KeyStroke.getKeyStroke(KeyEvent.VK_ENTER,0,true),
            JComponent.WHEN_ANCESTOR_OF_FOCUSED_COMPONENT
        ); 
    }
    
