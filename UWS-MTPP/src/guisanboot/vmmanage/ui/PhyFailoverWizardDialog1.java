    private void recordUWSRpt( boolean initOk ){
        String msg;

        Audit audit = view.audit.registerAuditRecord( host.getID(), MenuAndBtnCenterForMainUi.FUNC_WIN_PHY_FAILOVER );
        if( initOk ){
            audit.setEventDesc( "Netboot host: " + host.getName()+ " successfully." );
        }else{
            audit.setEventDesc( "Failed to netboot host: " + host.getName() );
        }
        view.audit.addAuditRecord( audit );

        UWSReport rpt = new UWSReport();

        Date date = new Date();
        GregorianCalendar today = new GregorianCalendar();
        today.setTime( date );
        
        int year  = today.get( Calendar.YEAR );
        String stime = year + "";
        int month = today.get( Calendar.MONTH ) + 1;
        if( month<10 ){
            stime+= "0" + month;
        }else{
            stime+=month;
        }
        int day   = today.get( Calendar.DAY_OF_MONTH );
        if( day <10 ){
            stime+= "0" + day;
        }else{
            stime+=day;
        }
        int hour  = today.get(Calendar.HOUR_OF_DAY);
        if( hour <10 ){
            stime+= "0" + hour;
        }else{
            stime+=hour;
        }
        int min   = today.get(Calendar.MINUTE);
        if( min <10 ){
            stime +="0"+min;
        }else{
            stime+=min;
        }
        int sec   = today.get(Calendar.SECOND);
        if( sec <10 ){
            stime+="0"+sec;
        }else{
            stime+=sec;
        }
        
        rpt.setSTime( stime ); 
        rpt.setClntName( host.getName() );
        
        rpt.setTaskClntId(  host.getID() );
        rpt.setTask_clnt_type(  DestAgent.TYPE_ORI_HOST );
        rpt.setTask_clnt_mac( DhcpClientInfo.getMacStr( this.bootMac ) ); // 记录网启所用的MAC
        rpt.setTask_clnt_dhcp_ip( this.getDhcpIP() ); // 记录dhcp分配的ip

        if( this.isSwitchDisk ){
            rpt.setTType( UWSReport.TASK_TYPE_SWITCH_L2I );
        }else{
            rpt.setTType( UWSReport.TASK_TYPE_L2I );
        }
        rpt.setTResult( initOk? UWSReport.TASK_RESULT_END:UWSReport.TASK_RESULT_FAIL );
        
        msg = logBuf.toString();
        if( msg.getBytes().length > 1014 ){
            byte[] arr = msg.getBytes();
            String newmsg = new String( arr,0,1014 );
            rpt.setTaskMsg( initOk? getTaskMsgForSuccess() : newmsg );
        }else{
            rpt.setTaskMsg( initOk? getTaskMsgForSuccess() : msg );
        }
        
        String fname = generateUWSRptTmpFile( rpt.prtMe() );
        if( fname != null ){
            if( !view.initor.mdb.addReport( fname ) ){
                // save in local host 
                saveUWSRpt( rpt.prtMe(), ResourceCenter.UWS_L2I_RPT + stime );
            }
        }else{
            // save in local host
            saveUWSRpt( rpt.prtMe(), ResourceCenter.UWS_L2I_RPT + stime );
        }
    }
    
