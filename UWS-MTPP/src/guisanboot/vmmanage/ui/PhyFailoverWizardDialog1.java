    private void setupServiceTable(){
        boolean stopAllService = false;
        
        if( servList == null ){ // 先将server table布置好
            // 获取系统当前的所有服务以及初始化时保存的服务的合集
            HashMap map = new HashMap();
            boolean isOk = view.initor.mdb.getOSService( host.getIP(),host.getPort(),"",ResourceCenter.CMD_TYPE_CMDP );
            if( isOk ){
                this.addService( map, view.initor.mdb.getOSService() );
            }
            // 再获取服务器上保存的
            isOk = view.initor.mdb.getOSService( ResourceCenter.CLT_IP_CONF+"/"+host.getID() + ResourceCenter.CONF_SERVICE,ResourceCenter.CMD_TYPE_CMDP );
            if( isOk ){
                this.addService(map, view.initor.mdb.getOSService() );
            }

            // 再获取volMap上绑定的服务（它们没有描述和路径）
            this.addService( map, view.initor.mdb.getServiceOfVolMapOnClntID( host.getID() ) );

            if( !host.isStopAllBaseServ() ){
                stopAllService = false;
                this.getServiceForCMDP( map );
            }else{
                stopAllService = true;
            }

            if( stopAllService ){
                selectServPane.setStopAllService( true );
                selectServPane.clickJRadioBtn1();
            }else{
                selectServPane.setStopPartialService( true );
                selectServPane.clickJRadioBtn2();
            }

            selectServPane.setupServiceTable( map );
        }
    }
    
