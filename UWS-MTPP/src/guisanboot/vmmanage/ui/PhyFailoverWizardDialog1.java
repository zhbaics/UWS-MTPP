    private void addService( HashMap map,Vector serviceList ){
        Service service;

        int size = serviceList.size();
        for( int i=0; i<size; i++ ){
            Object serObj = serviceList.get(i);
            if( serObj instanceof Service ){
                service = (Service)serviceList.get(i);
            }else{
                service = new Service( (String)serObj );
            }
            if( map.get( service.getServName() ) == null ){
                map.put( service.getServName(), service );
            }
        }
    }
    
    private void getServiceForCMDP( HashMap map ){
         ArrayList<PPProfileItem> itemList = view.initor.mdb.getPPProfileItem( host.getID() );
         
         int size = itemList.size();
         for( int i=0; i<size; i++ ){
             PPProfileItem item = itemList.get(i);
             String[] sList = item.getVolMap().getServices();
             for( int j=0; j<sList.length; j++ ){
                 if( sList[j].equals("") ) continue;

                Object serv = map.get( sList[j] );
                if( serv != null ){
                    Service servObj = (Service)serv;
                    servObj.setSeled(  true );
                }
             }
         }
    }

