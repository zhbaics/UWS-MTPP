    DefaultListModel model = new DefaultListModel();
    JList diskList = new JList( model );

    DefaultListModel model1 = new DefaultListModel();
    JList seldiskList = new JList( model1 );

    SanBootView view;

    PPProfileItem[] ret = null;

    private void myInit( SanBootView view,ArrayList<PPProfileItem> selDisks,ArrayList<PPProfileItem> unSelDisks ){
        this.view = view;

        jScrollPane1.getViewport().add( diskList, null );
        jScrollPane1.getViewport().setBackground( Color.white );
        diskList.setCellRenderer( new VolumeRenderer() );
     
        jScrollPane2.getViewport().add( seldiskList, null );
        jScrollPane2.getViewport().setBackground( Color.white );
        //seldiskList.setSelectionMode( ListSelectionModel.SINGLE_SELECTION );
        seldiskList.setCellRenderer( new VolumeRenderer() );

        setupDiskComboBox( unSelDisks );
        setupSelDiskComboBox( selDisks );
        
        setupLanguage();
    }

    private void setupDiskComboBox(ArrayList<PPProfileItem> unSelDisks ){
        this.model.removeAllElements();

        int size = unSelDisks.size();
        for( int i=0; i<size; i++ ){
            model.addElement( unSelDisks.get( i ) );
        }
    }

    private void setupSelDiskComboBox(ArrayList<PPProfileItem> selDisks ){
        this.model1.removeAllElements();

        int size = selDisks.size();
        for( int i=0; i<size; i++ ){
            model1.addElement( selDisks.get( i ) );
        }
    }

    private void setupLanguage(){
        this.setTitle(SanBootView.res.getString("EditVolumeDialog.title") );
        this.jButton1.setText(SanBootView.res.getString("common.button.add1") );
        this.jButton1.setIcon( ResourceCenter.BTN_ICON_ADD );
        this.jButton2.setText(SanBootView.res.getString("common.button.del") );
        this.jButton2.setIcon( ResourceCenter.BTN_ICON_REMOVE );
        this.jButton3.setText(SanBootView.res.getString("common.button.finish") );
        this.jButton4.setText(SanBootView.res.getString("common.button.cancel") );

        TitledBorder border = (TitledBorder)this.jPanel1.getBorder();
        border.setTitle( SanBootView.res.getString("EditVolumeDialog.border.disklist") );

        border =(TitledBorder)this.jPanel4.getBorder();
        border.setTitle( SanBootView.res.getString("EditVolumeDialog.border.seldisklist") );
    }
    
    private void do_add(){
        int[] indexs = this.diskList.getSelectedIndices();
        if( indexs == null || indexs.length<=0 ) {
            JOptionPane.showMessageDialog(this,
                SanBootView.res.getString("EditVolumeDialog.erorr.notSel")
            );
            return;
        }

        int len = indexs.length;
        for( int i=len-1; i>=0; i-- ){
            if( indexs[i] < 0  ) continue;
            PPProfileItem item = (PPProfileItem)model.getElementAt( indexs[i] );
            model1.addElement( item );
            model.remove( indexs[i] );
        }
    }

    private void do_remove(){
        int[] indexs = this.seldiskList.getSelectedIndices();
        if( indexs == null || indexs.length<=0 ) {
            JOptionPane.showMessageDialog(this,
                SanBootView.res.getString("EditVolumeDialog.erorr.notSel")
            );
            return;
        }

        int len = indexs.length;
        for( int i=len-1; i>=0; i-- ){
            if( indexs[i] < 0  ) continue;
            PPProfileItem item = (PPProfileItem)model1.getElementAt( indexs[i] );
            model.addElement( item );
            model1.remove( indexs[i] );
        }
    }

    private void do_finish(){
        int size = model1.size();
        if( size <=0 ){
            JOptionPane.showMessageDialog(this,
                SanBootView.res.getString("EditVolumeDialog.errmsg.notselected")
            );
            return;
        }

        this.ret = new PPProfileItem[size];
        for( int i=0; i<size; i++ ){
            ret[i] = (PPProfileItem)model1.get( i );
        }

        this.dispose();
    }
    
    public PPProfileItem[] getValues(){
        return ret;
    }
