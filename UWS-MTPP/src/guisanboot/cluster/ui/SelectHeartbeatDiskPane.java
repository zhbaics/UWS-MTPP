    public void setupComboBox( Vector<BindOfPartandVol> list ){
        jComboBox1.removeAllItems();
        int size = list.size();
        for( int i=0; i<size; i++ ){
            BindOfPartandVol binder = (BindOfPartandVol)list.get(i);
            PartitionWrapper pw = new PartitionWrapper( binder.part );
            jComboBox1.addItem( pw );
        }
    }
    
    public void setSelectedHbDsk( String hbdsk_String ){
        DefaultComboBoxModel model = (DefaultComboBoxModel)jComboBox1.getModel();
        int size = model.getSize();
        for( int i=0; i<size; i++ ){
            PartitionWrapper pw = (PartitionWrapper)model.getElementAt(i);
            if( pw.toString().trim().equals( hbdsk_String.trim() ) ){
                model.setSelectedItem( pw );
                break;
            }
        }
    }
    
    public boolean checkValidity(){
        Object obj = this.jComboBox1.getSelectedItem();
        if( obj == null ){
            JOptionPane.showMessageDialog(this,
                SanBootView.res.getString("PhyInitWinClusterWizardDialog.error.noneHbDsk")
            );
            return false;
        }else{
            return true;
        }
    }

    public SystemPartitionForWin getSelectedHbDsk(){
        return ((PartitionWrapper)this.jComboBox1.getSelectedItem()).getPart();
    }

    public String getSelectedHbDskString(){
        return this.jComboBox1.getSelectedItem().toString();
    }
