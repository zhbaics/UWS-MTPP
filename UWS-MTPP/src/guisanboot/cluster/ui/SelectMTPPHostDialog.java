    private void myInit( ArrayList<BootHost> hostList ){
        setupLanguage();

        int size = hostList.size();
        for( int i=0; i<size; i++ ){
            BootHost host = hostList.get(i);
            HostWrapper wrapper = new HostWrapper( host );
            jComboBox1.addItem( wrapper );
        }
    }

    private void setupLanguage(){
        this.setTitle( SanBootView.res.getString("SelectMTPPHostDialog.title"));
        this.jLabel1.setText( SanBootView.res.getString("SelectMTPPHostDialog.label.mtppHost"));
        this.jButton1.setText( SanBootView.res.getString("common.button.ok"));
        this.jButton2.setText( SanBootView.res.getString("common.button.cancel"));
    }

    Object ret;
    private void ok_processbtn(){
        ret = this.jComboBox1.getSelectedItem();
        this.dispose();
    }

    private void cancel_processbtn(){
        ret = null;
        this.dispose();
    }
    
    public Object getSelectedHost(){
        return ret;
    }
