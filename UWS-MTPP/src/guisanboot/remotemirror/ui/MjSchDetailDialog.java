    private void initUI( MirrorJobSch mjSch ){
        java.util.GregorianCalendar cal = new java.util.GregorianCalendar();
        int weekDay = cal.get( java.util.GregorianCalendar.DAY_OF_WEEK );
        int week_day = ( weekDay == 1 )?  7: ( weekDay-1 );
        if( week_day == 1 ){
            jLabel1.setForeground( Color.RED );
        }else if( week_day == 2 ) {
            jLabel2.setForeground( Color.RED );
        }else if( week_day == 3 ) {
            jLabel3.setForeground( Color.RED );
        }else if( week_day == 4 ) {
            jLabel4.setForeground( Color.RED );
        }else if( week_day == 5 ) {
            jLabel5.setForeground( Color.RED );
        }else if( week_day == 6 ) {
            jLabel6.setForeground( Color.RED );
        }else if( week_day == 7 ) {
            jLabel7.setForeground( Color.RED );
        }
        
        this.jLabel8.setText( mjSch.getSmpleStr( mjSch.getScheduler_mon() ) );
        this.jLabel9.setText( mjSch.getSmpleStr( mjSch.getScheduler_tue() ) );
        this.jLabel10.setText( mjSch.getSmpleStr( mjSch.getScheduler_wed() ) );
        this.jLabel11.setText( mjSch.getSmpleStr( mjSch.getScheduler_thu() ) );
        this.jLabel12.setText( mjSch.getSmpleStr( mjSch.getScheduler_fri() ) );
        this.jLabel13.setText( mjSch.getSmpleStr( mjSch.getScheduler_sat() ));
        this.jLabel14.setText( mjSch.getSmpleStr( mjSch.getScheduler_sun() ) );
    }

